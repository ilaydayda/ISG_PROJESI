def h_kodu_aciklama_getir(kod):
   
    if not kod:
        return None

    kod_temiz = kod.strip().replace(" ", "")

    # Önce tam eşleşmeye bak (örn. "H360FD", "H300+H310")
    if kod_temiz in H_KODLARI_SOZLUGU:
        return H_KODLARI_SOZLUGU[kod_temiz]
    
    # Büyük harfe çevirip tekrar dene (varyant harfleri hariç temel kod için)
    kod_buyuk = kod_temiz.upper()
    if kod_buyuk in H_KODLARI_SOZLUGU:
        return H_KODLARI_SOZLUGU[kod_buyuk]
    
    return None